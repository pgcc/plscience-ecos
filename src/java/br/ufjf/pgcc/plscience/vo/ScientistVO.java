package br.ufjf.pgcc.plscience.vo;

/**
 *
 * @author Fran
 */
public class ScientistVO {
    private String hasInstitutionFiliation;
    private String hasCompleteName;
    private String hasEmail;

    public ScientistVO() {
    }

    public String getHasInstitutionFiliation() {
        return hasInstitutionFiliation;
    }

    public void setHasInstitutionFiliation(String hasInstitutionFiliation) {
        this.hasInstitutionFiliation = hasInstitutionFiliation;
    }

    public String getHasCompleteName() {
        return hasCompleteName;
    }

    public void setHasCompleteName(String hasCompleteName) {
        this.hasCompleteName = hasCompleteName;
    }

    public String getHasEmail() {
        return hasEmail;
    }

    public void setHasEmail(String hasEmail) {
        this.hasEmail = hasEmail;
    }
    
    
}
