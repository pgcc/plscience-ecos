package br.ufjf.pgcc.plscience.socialNetworkAnalysis;

/**
 *
 * @author phillipe
 */
public class NodeSNCoordinates {
    private int xMin;
    private int xMax;
    private int ymin;
    private int ymax;

    /**
     * @return the xMin
     */
    public int getxMin() {
        return xMin;
    }

    /**
     * @param xMin the xMin to set
     */
    public void setxMin(int xMin) {
        this.xMin = xMin;
    }

    /**
     * @return the xMax
     */
    public int getxMax() {
        return xMax;
    }

    /**
     * @param xMax the xMax to set
     */
    public void setxMax(int xMax) {
        this.xMax = xMax;
    }

    /**
     * @return the ymin
     */
    public int getYmin() {
        return ymin;
    }

    /**
     * @param ymin the ymin to set
     */
    public void setYmin(int ymin) {
        this.ymin = ymin;
    }

    /**
     * @return the ymax
     */
    public int getYmax() {
        return ymax;
    }

    /**
     * @param ymax the ymax to set
     */
    public void setYmax(int ymax) {
        this.ymax = ymax;
    }
}
