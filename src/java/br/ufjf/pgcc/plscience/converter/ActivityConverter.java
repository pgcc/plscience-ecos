package br.ufjf.pgcc.plscience.converter;

import br.ufjf.pgcc.plscience.model.Activity;
import javax.faces.bean.ManagedBean;
import javax.faces.component.UIComponent;
import javax.faces.context.FacesContext;
import javax.faces.convert.Converter;
import javax.faces.convert.FacesConverter;

/**
 *
 * @author Tassio
 */
@FacesConverter("activityConverter")
@ManagedBean
public class ActivityConverter implements Converter {
    @Override
    public Object getAsObject(FacesContext fc, UIComponent uic, String value) {
        if (value != null && !value.isEmpty()) {
            return (Activity) uic.getAttributes().get(value);
        }
        return null;
    }

    @Override
    public String getAsString(FacesContext fc, UIComponent uic, Object value) {
        if (value instanceof Activity) {
            Activity pergunta = (Activity) value;
            if (pergunta != null && pergunta instanceof Activity && pergunta.getIdActivity() != null) {
                uic.getAttributes().put( pergunta.getIdActivity().toString(), pergunta);
                return pergunta.getIdActivity().toString();
            }
        }
        return "";
    }
}
    
